#include<stdio.h>

int main() {
    float scoer = 90.5;
    printf("Score: %.1f\n", scoer);

    int score = 90.5;
    printf("Score: %d\n", score);

    return 0;
}

// 1.ผลลัพธ์ที่ได้ต่างกันอย่างไร? คือ int จะไม่เก็บทศนิยม แต่ float จะเก็บทศนิยมได้
// 2.ทำไมต้องเลือก Data Type ให้ถูกต้อง? เพื่อค่าที่ถูกต้องและแม่นยำ
// 3.สรุป:ถ้า int จะเหมาะกับข้อมูลตัวเลขที่ไม่เก็บทศนิยม แต่ float จะเหมาะกับข้อมูลตัวเลขที่เก็บทศนิยมได้